export function textPrimary100(color) {
  switch (color) {
    case 'SD':
      return 'text-sd-100 hover:text-sd-300'
    case 'SMP':
      return 'text-smp-100 hover:text-smp-300'
    case 'SMA':
      return 'text-sma-100 hover:text-sma-300'
    case 'ISLAMIC':
      return 'text-islamic-100 hover:text-islamic-300'
    default:
      return 'text-sd-100 hover:text-sd-300'
  }
}

